export default function Phases() {
    const [openPhase, setOpenPhase] = useState<number | null>(null);

    const togglePhase = (id: number) => {
        setOpenPhase(openPhase === id ? null : id);
    };

    return (
        <section id="content" className="py-20 relative overflow-hidden">
            <div className="container mx-auto px-4 max-w-6xl">
                <div className="text-center mb-16">
                    <h2 className="text-4xl lg:text-5xl font-bold font-cairo mb-4">
                        خارطة الطريق <span className="text-[#1E3A8A]">الشاملة</span>
                    </h2>
                    <p className="text-gray-500">11 مرحلة تأخذك من الصفر إلى القمة</p>
                </div>

                <div className="space-y-6">
                    {PHASES.map((phase) => (
                        <PhaseCard
                            key={phase.id}
                            phase={phase}
                            isOpen={openPhase === phase.id}
                            onToggle={() => togglePhase(phase.id)}
                        />
                    ))}
                </div>
            </div>
        </section>
    );
}

function PhaseCard({ phase, isOpen, onToggle }: { phase: Phase, isOpen: boolean, onToggle: () => void }) {
    return (
        <div className={clsx(
            "rounded-2xl border transition-all duration-300 overflow-hidden",
            isOpen ? "bg-[#f4f6f8]/80 border-gray-300" : "bg-gray-50 border-gray-200 hover:border-gray-200"
        )}>
            {/* Header */}
            <div
                className="p-6 cursor-pointer flex flex-col md:flex-row items-center justify-between gap-4"
                onClick={onToggle}
            >
                <div className="flex items-center gap-6 flex-1">
                    <div
                        className="w-16 h-16 rounded-xl flex items-center justify-center text-2xl font-bold shrink-0"
                        style={{ backgroundColor: `${phase.color}20`, color: phase.color, border: `1px solid ${phase.color}40` }}
                    >
                        {phase.id}
                    </div>
                    <div className="text-right">
                        <div
                            className="text-xs font-bold tracking-widest mb-1 font-orbitron"
                            style={{ color: phase.color }}
                        >
                            PHASE {phase.id}: {phase.name}
                        </div>
                        <h3 className="text-2xl font-bold text-gray-900 mb-2">{phase.title}</h3>
                        <p className="text-gray-500 text-sm mb-1">{phase.subtitle}</p>
                        <p className="text-[#1E3A8A] text-xs font-bold">{phase.count} وحدة تدريبية مكثفة</p>
                    </div>
                </div>

                <button
                    className="px-6 py-2 rounded-full text-sm font-bold border transition-colors flex items-center gap-2"
                    style={{
                        borderColor: isOpen ? phase.color : 'rgba(255,255,255,0.1)',
                        backgroundColor: isOpen ? `${phase.color}10` : 'transparent',
                        color: isOpen ? phase.color : 'white'
                    }}
                >
                    {isOpen ? "إخفاء الوحدات" : "عرض الوحدات"}
                    {isOpen ? <ChevronUp size={16} /> : <ChevronDown size={16} />}
                </button>
            </div>

            {/* Grid Content */}
            {isOpen && (
                <div className="p-6 pt-0 border-t border-gray-200 mt-6">
                    <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6 pt-6">
                        {phase.modules.map((moduleName: string, i: number) => (
                            <ModuleCard key={i} index={i} color={phase.color} title={moduleName} phaseId={phase.id} />
                        ))}
                    </div>
                </div>
            )}
        </div>
    );
}

